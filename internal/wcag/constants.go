// WCAG 2.1 contrast ratio thresholds
const (
	// ContrastRatioAA is the minimum contrast ratio for AA compliance (small text)
	ContrastRatioAA = 4.5

	// ContrastRatioAAA is the minimum contrast ratio for AAA compliance (small text)
	ContrastRatioAAA = 7.0

	// ContrastRatioAALarge is the minimum contrast ratio for AA compliance (large text)
	ContrastRatioAALarge = 3.0

	// ContrastRatioAAALarge is the minimum contrast ratio for AAA compliance (large text)
	ContrastRatioAAALarge = 4.5

	// MinimumContrast is the absolute minimum contrast ratio for accessibility
	MinimumContrast = 3.0
)
